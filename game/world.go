package game

type Tile int

const (
	TileFloor Tile = iota
	TileWall
	TileWater
	TileBush
)

type RoomID struct {
	X, Y int
}

type RoomData struct {
	Tiles       [][]Tile
	EnemySpawns [][2]int // row, col
}

func TileBlocksMove(t Tile) bool {
	return t == TileWall || t == TileWater
}

func parseRoom(lines []string) ([][]Tile, [][2]int) {
	var spawns [][2]int
	grid := make([][]Tile, len(lines))
	for r, row := range lines {
		out := make([]Tile, len(row))
		for c := 0; c < len(row); c++ {
			switch row[c] {
			case 'o':
				spawns = append(spawns, [2]int{r, c})
				out[c] = TileFloor
			case '#':
				out[c] = TileWall
			case '~':
				out[c] = TileWater
			case '*':
				out[c] = TileBush
			default:
				out[c] = TileFloor
			}
		}
		grid[r] = out
	}
	return grid, spawns
}

func normalizeRoomLines(lines []string) []string {
	w, h := RoomInnerW, RoomInnerH
	out := make([]string, h)
	for r := 0; r < h; r++ {
		if r < len(lines) {
			row := lines[r]
			if len(row) >= w {
				out[r] = row[:w]
			} else {
				s := row
				for len(s) < w {
					s += "."
				}
				out[r] = s[:w]
			}
		} else {
			if r == 0 || r == h-1 {
				bs := make([]byte, w)
				for i := range bs {
					bs[i] = '#'
				}
				out[r] = string(bs)
			} else {
				mid := make([]byte, w)
				mid[0], mid[w-1] = '#', '#'
				for i := 1; i < w-1; i++ {
					mid[i] = '.'
				}
				out[r] = string(mid)
			}
		}
	}
	return out
}

