"""Tile kinds, room layouts, and overworld graph (Zelda-style screen transitions)."""

from __future__ import annotations

from dataclasses import dataclass, field
from enum import IntEnum
from typing import Dict, List, Tuple

from .constants import ROOM_INNER_H, ROOM_INNER_W


class Tile(IntEnum):
    FLOOR = 0
    WALL = 1
    WATER = 2
    BUSH = 3  # walkable, decorative


def _parse_room(
    lines: List[str],
) -> Tuple[List[List[Tile]], List[Tuple[int, int]]]:
    """Return tile grid and list of enemy spawn (row, col) from 'o' markers."""
    spawns: List[Tuple[int, int]] = []
    grid: List[List[Tile]] = []
    for r, row in enumerate(lines):
        out_row: List[Tile] = []
        for c, ch in enumerate(row):
            if ch == "o":
                spawns.append((r, c))
                out_row.append(Tile.FLOOR)
            elif ch == "#":
                out_row.append(Tile.WALL)
            elif ch == "~":
                out_row.append(Tile.WATER)
            elif ch == "*":
                out_row.append(Tile.BUSH)
            else:
                out_row.append(Tile.FLOOR)
        grid.append(out_row)
    return grid, spawns


