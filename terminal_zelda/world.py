def normalize_room_lines(lines: List[str]) -> List[str]:
    w, h = ROOM_INNER_W, ROOM_INNER_H
    out: List[str] = []
    for r in range(h):
        if r < len(lines):
            row = lines[r]
            if len(row) >= w:
                out.append(row[:w])
            else:
                out.append(row.ljust(w, ".")[:w])
        else:
            if r == 0 or r == h - 1:
                out.append("#" * w)
            else:
                out.append("#" + "." * (w - 2) + "#")
    return out


@dataclass
class RoomData:
    tiles: List[List[Tile]]
    enemy_spawns: List[Tuple[int, int]] = field(default_factory=list)


def load_world() -> Dict[Tuple[int, int], RoomData]:
    world: Dict[Tuple[int, int], RoomData] = {}
    for coord, raw in RAW_ROOMS.items():
        norm = normalize_room_lines(raw)
        tiles, spawns = _parse_room(norm)
        world[coord] = RoomData(tiles=tiles, enemy_spawns=spawns)
    return world


def tile_blocks_move(t: Tile) -> bool:
    return t in (Tile.WALL, Tile.WATER)
